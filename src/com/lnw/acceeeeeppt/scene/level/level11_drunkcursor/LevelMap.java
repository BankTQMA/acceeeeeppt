    public LevelMap() {
        walls = new ArrayList<>();
        generateMap();
    }

    private void generateMap() {
        for (int row = 0; row < mapGrid.length; row++) {
            for (int col = 0; col < mapGrid[row].length; col++) {
                
                int tile = mapGrid[row][col];
                int x = col * TILE_SIZE;
                int y = row * TILE_SIZE;

                if (tile == 1) {
                    walls.add(new Rectangle(x, y, TILE_SIZE, TILE_SIZE));
                } else if (tile == 2) {
                    startArea = new Rectangle(x, y, TILE_SIZE, TILE_SIZE);
                } else if (tile == 3) {
                    agreeButton = new Rectangle(x, y, TILE_SIZE, TILE_SIZE);
                }
            }
        }
    }
